package com.lanou.bookstore.book.domain;

public class CategoryBean {

    private int cid;
    private String cname;

    public CategoryBean() {
    }

    public int getCid() {
        return cid;
    }

    public void setCid(int cid) {
        this.cid = cid;
    }

    public String getCname() {
        return cname;
    }

    public void setCname(String cname) {
        this.cname = cname;
    }
}
